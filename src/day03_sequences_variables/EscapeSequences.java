package day03_sequences_variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello everyone \nmy name is Hasan");
        System.out.println("\tI am in TEXAS");
        System.out.println("\n\nI love java programming");

        System.out.println("\\"); //prints one back slash
        System.out.println("//"); //prints two front slash

        System.out.println("\'");  // prints single quote
        System.out.println("\"");  // prints double quote (aka speech mark)

    }
}
